CREATE DATABASE FASTFOOD

USE FASTFOOD
CREATE TABLE FastFoodNutrition (
    Company NVARCHAR(100),
    Item NVARCHAR(255),
    Calories INT,
    TotalFat_g INT,
    SaturatedFat_g FLOAT,
    TransFat_g FLOAT,
    Cholesterol_mg INT,
    Sodium_mg INT,
    Carbs_g INT,
    Fiber_g INT,
    Sugars_g INT,
    Protein_g INT,
    WeightWatchers_Points FLOAT
);




